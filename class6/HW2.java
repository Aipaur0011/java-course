package JAVABatch15.class32.class6;
/*
Print numbers from 1 to 100 in 1 line with space
 */


public class HW2 {
    public static void main(String[] args) {

        int number=1;
        while(number<101) {

            System.out.print(" " +number+ " ");
            number++;
        }

        int number1=1;
        while(number1<101) {

            System.out.print(number1+ "\t");
            number1++;
        }




    }
}
